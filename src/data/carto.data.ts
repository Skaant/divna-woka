const EARTH_CIRCUM_METERS = 40000000;

const LAT_MIN = 44.514354;
const LAT_MAX = 44.520106;
const LONG_MIN = 5.672893;
const LONG_MAX = 5.677415;
const MICRO_COORD_LONG_METERS =
  (EARTH_CIRCUM_METERS *
    Math.cos((((LAT_MIN + LAT_MAX) / 2) * Math.PI) / 180)) /
  360 /
  1000000;
const MICRO_COORD_LAT_METERS = EARTH_CIRCUM_METERS / 360 / 1000000;
const PIXELS_BY_METER = 2;
export const CARTO_DATA = {
  LAT_MIN,
  LAT_MAX,
  LONG_MIN,
  LONG_MAX,
  MICRO_COORD_LAT_METERS,
  MICRO_COORD_LONG_METERS,
  PIXELS_BY_METER,
  WIDTH:
    (LONG_MAX - LONG_MIN) * 1000000 * MICRO_COORD_LONG_METERS * PIXELS_BY_METER,
  HEIGHT:
    (LAT_MAX - LAT_MIN) * 1000000 * MICRO_COORD_LAT_METERS * PIXELS_BY_METER,
  LAT_LONG_METERS_PERCENTS:
    (((LAT_MAX - LAT_MIN) * MICRO_COORD_LAT_METERS) /
      ((LONG_MAX - LONG_MIN) * MICRO_COORD_LONG_METERS)) *
    100,
};
